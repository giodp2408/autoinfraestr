# Ejercicio 5. Escribir un programa que pregunte al usuario por el número de horas trabajadas y 
# el coste por hora. Después debe mostrar por pantalla la paga que le corresponde.

hora = int(input("¿Cuantas horas trabajo? "))
coste = int(input("¿Cuanto es el coste por hora? "))
resul = hora * coste
print(resul)